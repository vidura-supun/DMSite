            <p>Nearly every part of our country experiences periods of reduced rainfall. If we plan for drought, then we can
                enjoy the benefits of normal or rainy years and not get caught unprepared in dry years.</p>
            <h2>Before a Drought</h2>
            <p>Strategies for drought preparedness focus mainly on water conservation. Make these practices a part of your daily
                life and help preserve this essential resource.</p>
            <h3>Indoor Water Conservation Tips Prior to a Drought</h3>
            <h2>During a Drought or a water cut in your area</h2>
            <p>Always observe state and local restrictions on water use during a drought. If restricted, for example, do not
                water your lawn, wash your car, or other non-essential uses, to help ensure there is enough water for essential
                uses.
             </p>
            <ul>
                <li>Never pour water down the drain when there may be another use for it. For example, use it to water your indoor
                    plants or garden.</li>
                <li>Repair dripping faucets by replacing washers. One drop per second wastes 2,700 gallons of water per year.</li>
                <li>Check all plumbing for leaks and have any leaks repaired by a plumber.</li>
                <li>Retrofit all household faucets by installing aerators with flow restrictors.</li>
                <li>Install an instant hot water heater on your sink.</li>
                <li>Insulate your water pipes to reduce heat loss and prevent them from breaking.</li>
                <li>Install a water-softening system only when the minerals in the water would damage your pipes. Turn the softener
                    off while on vacation.</li>
                <li>Choose appliances that are more energy and water efficient.</li>
            </ul>
            <p>BATHROOM</p>
            <ul>
                <li>Consider purchasing a low-volume toilet that uses less than half the water of older models. Note: In many
                    areas, low-volume units are required by law.</li>
                <li>Install a toilet displacement device to cut down on the amount of water needed to flush. Place a one-gallon
                    plastic jug of water into the tank to displace toilet flow (do not use a brick, it may dissolve and loose
                    pieces may cause damage to the internal parts). Be sure installation does not interfere with the operating
                    parts.</li>
                <li>Replace your showerhead with an ultra-low-flow version.</li>
            </ul>
            <p>KITCHEN</p>
            <ul>
                <li>Start a compost pile as an alternate method of disposing of food waste or simply dispose of food in the garbage.
                    (Kitchen sink disposals require a lot of water to operate properly).</li>
            </ul>
            <h3>Outdoor Water Conservation Tips Prior to a Drought</h3>
            <p>GENERAL</p>
            <ul>
                <li>Check your well pump periodically. If the automatic pump turns on and off while water is not being used,
                    you have a leak.</li>
                <li>Plant native and/or drought-tolerant grasses, ground covers, shrubs, and trees. Once established, plants
                    adapted to your local climate do not need water as frequently and usually will survive a dry period without
                    watering. Small plants require less water to become established. Group plants together based on similar
                    water needs.</li>
                <li>Install irrigation devices that are the most water efficient for each use, such as micro and drip irrigation,
                    and soaker hoses.</li>
                <li>Use mulch to retain moisture in the soil. Mulch also helps control weeds that compete with landscape plants
                    for water.</li>
                <li>Avoid purchasing recreational water toys that require a constant stream of water.</li>
                <li>Avoid installing ornamental water features (such as fountains) unless they use re-circulated water.</li>
                <li>Consider rainwater harvesting where practical.</li>
                <li>Contact your local water provider for information and assistance.</li>
            </ul>
            <p>LAWN CARE</p>
            <ul>
                <li>Position sprinklers so water lands on the lawn and shrubs and not on paved areas.</li>
                <li>Repair sprinklers that spray a fine mist. Most misting issues result from a pressure problem, properly regulating
                    pressure in an irrigation system will prevent misting.</li>
                <li>Check sprinkler systems and timing devices regularly to be sure they operate properly.</li>
                <li>Raise the lawn mower blade to at least three inches or to its highest level. A higher cut encourages grass
                    roots to grow deeper, shades the root system, and holds soil moisture.</li>
                <li>Plant drought-resistant lawn seed. Reduce or eliminate lawn areas that are not used frequently.</li>
                <li>Avoid over-fertilizing your lawn. Applying fertilizer increases the need for water. Apply fertilizers that
                    contain slow-release, water-insoluble forms of nitrogen.</li>
                <li>Choose a water-efficient irrigation system such as drip irrigation for your trees, shrubs, and flowers.</li>
                <li>Turn irrigation down in fall and off in winter. Water manually in winter only if needed.</li>
                <li>Put a layer of mulch around trees and plants to reduce evaporation and keep the soil cool. Organic mulch
                    also improves the soil and prevents weeds.</li>
