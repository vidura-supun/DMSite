    <h2>Hurricanes </h2>
    <p>Hurricanes are violent tropical storms. These extreme storms occur when winds revolve around a centre of low pressure. In the centre, called the eye, there is often a calm area of blue sky. </p>
    <p><strong>Hurricanes:</strong> </p>
    <ul>
    
        <li>Are bigger and cause more widespread damage than tornadoes (a very large system can be up to 1,000 kilometers wide). </li>
        <li>Wield very strong winds – of at least 120 kilometers per hour – around the &ldquo;eye&rdquo; accompanied by torrential rains. </li>
        <li>Can bring heavy rain and cause significant flooding. </li>
        <li>Can often be tracked several days in advance of landfall. </li>
        <li>Usually move slowly and can batter communities for several hours. </li>
    </ul>
    <p><strong>What to do </strong> </p>
    <ul>
        <li>During hurricane season, pay attention to weather forecasts and warnings. </li>
        <li>If you live on the coast or in a low-lying area near the coast, move inland and to higher ground. The high winds create huge waves at sea which can be very damaging when combined with a storm surge (see Storm Surges section). </li>
        <li>Do not go down to the water to watch the storm. Most fatalities during hurricanes occur as a result of being caught in large waves, storm surges or flood waters. </li>
        <li>If the eye of the hurricane passes over, there will be a lull in the wind lasting from several minutes to half an hour. Stay in a safe place. Make emergency repairs only and remember that once the eye has passed, the winds will return from the opposite direction with possibly even greater force. </li>
        <li>Listen for reports from authorities on your crank or battery powered radio. </li>
        <li>On a farm, it may be better to leave livestock unsheltered. During past hurricanes some animals left outside suffered less injury than those in shelters, which were injured by collapsing structures and flying objects that may have been avoided outside. </li>
    </ul>

    <h2>Storm surges </h2>
    <p>A storm surge is an abnormally high coastal water level caused by strong winds and low air pressure during storms. </p>
    <p><strong>Storm surges:</strong> </p>
    <ul>
        <li>Occur on all of Sri lanka's coasts, </li>
        <li>Occur with severe storms such as hurricanes </li>
        <li>Can damage buildings, docks, boats and other structures near the shoreline. </li>
    </ul>
    <p><strong>What to do </strong> </p>
    <ul>
        <li>Your property may be prone to flooding from storm surges. If so, do not store valuables and emergency equipment in your basement or lower floor. Consider removing exterior doors and windows to your basement and sealing holes and cracks. </li>
        <li>Consider securing small structures such as cottages and mobile homes to a foundation to prevent them from being floated off their footings. If possible, seek shelter in a more secure building.</li>
        <li>Storm surges are predictable and are typically forecast as part of coastal storm warnings. Monitor weather forecasts. </li>
        <li>If flooding is predicted, be prepared to turn off household power and gas. Evacuate when instructed to do so by local authorities.</li>
    </ul>
    <h2><span>Thunderstorms, lightning and hail</span> </h2>
    <p>Thunderstorms are often accompanied by high winds, hail, lightning, heavy rain and in rare cases can produce tornadoes. Hail is formed when updrafts in thunderclouds carry raindrops upward into extremely cold areas of the atmosphere, where they freeze and merge into lumps of ice. </p>
    <p><strong>Thunderstorms, lightning and hail:</strong> </p>
    <ul>
        <li>Thunderstorms and lightning occur rarely in sri lanka. </li>
        <li>Thunderstorms are usually over within an hour, although a series of thunderstorms can last several hours. </li>
        <li>Hailstorms occur across Canada, mostly from May to October. They are most frequent in Alberta, the southern Prairies and in southern Ontario.</li>
        <li>Some hailstones are the size of peas while others can be as big as grapefruits. </li>
        <li>Hail comes down at great speed, especially when accompanied by high winds and can cause serious injuries and damages. </li>
    </ul>
    <p><strong>What to do if outside </strong> </p>
    <ul>
        <li>If you are caught outside and you can see lightning or hear thunder, you are in danger of being hit. Seek shelter immediately either in an enclosed building or a hard-topped vehicle. There is no safe place outside in a thunderstorm. </li>
        <li>If caught outside far from a safe location, stay away from tall objects, such as trees, poles, wires and fences. Take shelter in a low lying area.</li>
        <li>Wait 30 minutes after the last rumble of thunder before going outside again.</li>







