    <p><strong>When a storm is imminent </strong></p>
    <ul>
        <li>When a severe storm is on the horizon, vipathlk will issue weather warnings through the website,  . Radio and television stations will also broadcast Environment Canada weather statements. Pay attention to that information.</li>
        <li>Always check the weather forecast before heading out on the water. Do not go boating in a storm. If you are on the water and see bad weather approaching, head for shore immediately. Remember to file a sail plan with a responsible person, and frequently monitor the VHF marine or Weatheradio broadcast throughout your trip. </li>
        <li>Secure everything that might be blown around or torn loose – indoors and outdoors. Flying objects such as garbage cans and lawn furniture can injure people and damage property. </li>
        <li>Consider going to the sheltered area that you and your family identified in your emergency plan. </li>
    </ul>
    <p><strong>During a storm</strong> </p>
    <ul>
        <li>If you are indoors during a storm, stay away from windows, doors and fireplaces.</li>
        <li>If you are advised by officials to evacuate, do so. Delay may make later evacuation difficult or impossible. Take your emergency kit with you. </li>
        <li>If indoors, you can use a cellular or cordless telephone during a severe storm, but it is not safe to use a corded telephone. </li>
        <li>If you are in a car, stop the car away from trees or power lines that might fall on you. Avoid the base of steep or unstable slopes and low areas prone to flooding. Stay inside the car. </li>
    </ul>
