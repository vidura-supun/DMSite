                        <li>
                            <p>If there is a chance of flash flooding, move immediately to higher ground.</p>
                        </li>
                        <li>
                            <p>If floodwaters rise around your car but the water is not moving, abandon the car and move to higher ground.
                                Do not leave the car and enter moving water.</p>
                        </li>
                        <li>Avoid camping or parking along streams, rivers, and creeks during heavy rainfall. These areas can flood quickly
                            and with little warning.</li>
                    </ul>
