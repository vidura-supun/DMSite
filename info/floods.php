                    <p>Flooding is a temporary overflowing of water onto land that is normally dry. Flooding may happen with only a
                        few inches of water, or it may cover a house to the rooftop. There are many possible causes of floods including
                        heavy rain coastal storms and storm surge, waterway overflow from being blocked with garbage, or overflow  dams, or waste water systems, Flooding can occur slowly over many days or
                        happen very quickly with little or no warning,this is called flash floods.</p>
                  
                    
                    <h2>Basic Safety Tips</h2>
                    <ul>
                        <li>just turn Around,don't go near water, Don’t Drown!</li>
                        <li>Avoid walking or driving through flood waters.</li>
                        <li>Do not drive over bridges that are over fast moving floodwaters. Floodwaters can scour foundation material
                            from around the footings and make the bridge unstable.</li>
                        <li>
                            <p>Just 6 inches of moving water can knock you down, and one foot of moving water can sweep your vehicle
                                away.</p>
