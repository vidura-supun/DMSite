                                <strong>Prepare Your Home</strong>
                            </p>
                        </li>
                        <li>
                            <p>Bring in outdoor furniture and move important indoor items to the highest possible floor. This will help
                                protect them from flood damage.</p>
                        </li>
                        <li>
                            <p>Disconnect electrical appliances and do not touch electrical equipment if you are wet or standing in
                                water. You could be electrocuted.</p>
