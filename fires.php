      <h1>Before a wildfire</h1>
      <h2>How to prepare for a wildfire</h2>
     
       
        <li>Check for, and remove, fire hazards in and around your home, such as dried out branches, leaves and debris. </li>
        <li>Keep a good sprinkler in an accessible location. </li>
        <li>Learn fire safety techniques and teach them to members of your family. </li>
        <li>Have fire drills with your family on a regular basis. </li>
        <li>Maintain first-aid supplies to treat the injured until help arrives. </li>
        <li>Have an escape plan so that all members of the family know how to get out of the house quickly and safely. </li>
        <li>Have a emergency plan so family members can contact each other in case they are separated during an evacuation. </li>
        <li>Make sure all family members are familiar with the technique of &quot;STOP, DROP, AND&nbsp;ROLL&quot; in case of clothes catching on fire. </li>
        <li>Make sure every floor and all sleeping areas have smoke detectors. </li>
        <li>Consult with your local fire department about making your home fire-resistant. </li>
        <li>If you are on a farm/ranch, sheltering livestock may be the wrong thing to do because a wildfire could trap animals inside, causing them to burn alive. Leaving animals unsheltered is preferable, or if time and personal safety permits, evacuation away from the danger zone should be considered.</li>
      </ul>
      <h2>If you see a wildfire approaching your home</h2>
      <p>If you see a fire approaching your home or community, report it immediately by dialing 9-1-1 or your local emergency number. If it is safe, and there is time before the fire arrives, you should take the following action: </p>
      <ul>
        <li>Close all windows and doors in the house. </li>
        <li>Cover vents, windows, and other openings of the house with duct tape and/or precut pieces of plywood. </li>
        <li>Park your car, positioned forward out of the driveway. Keep car windows closed and have your valuables already packed in your car. </li>
        <li>Turn off propane or natural gas. Move any propane barbeques into the open, away from structures. </li>
        <li>Turn on the lights in the house, porch, garage and yard. <br>
          Inside the house, move combustible materials such as light curtains and furniture away from the windows. </li>
        <li>Place a ladder to the roof in the front of the house. </li>
        <li>Put lawn sprinklers on the roof of the house and turn on the water. </li>
        <li>Move all combustibles away from the house, including firewood and lawn furniture. </li>
        <li>Evacuate your family and pets to a safe location. </li>
        <li>Stay tuned to your local radio station for up-to-date information on the fire and possible road closures. </li>
      </ul>


      <h1>During a wildfire</h1>
      <ul>
          <li>Monitor local radio stations. </li>
          <li>Be prepared to evacuate at any time. If told to evacuate, do&nbsp;so. </li>
          <li>Keep all doors and windows closed in your home.</li>
          <li>Remove flammable drapes, curtains, awnings or other window coverings. </li>
          <li>Keep lights on to aid visibility in case smoke fills the house. </li>
          <li>If sufficient water is available, turn sprinklers on to wet the roof and any water-proof valuables. </li>
      </ul>

