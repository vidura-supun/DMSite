<h1>Tsunamis</h1>
<p>Tsunamis are a series of large waves that strike coastal areas. They can happen with little warning and result in flooding and damage to coastal communities. Thankfully, they are a rare event. Storm surges are more common. Residents, particularly those living in low-lying areas near the coastline, should prepare in advance. </p>
<h3>Early warning signs of a tsunami</h3>
<ul>
    <li>One of the signs of a potential tsunami is the occurrence of a very large earthquake that lasts for more than 20 seconds. If an area has been shaken by a very large earthquake, one should be on alert that shorelines located within the radius of the earthquake's epicentre, may be hit by a tsunami. </li>
    <li>A more immediate and ominous sign of an approaching tsunami is a rapid and unexpected recession of water levels below the expected low tide. This can occur minutes before the shoreline is struck by a tsunami and can be the only sign along coastlines that are located too far from the earthquake epicentre to have felt the shaking. </li>
    <li>A tsunami may also occur with very little warning. </li>
    <li>Natural Resources Canada's seismologists monitor for such events, around the clock. As soon as possible, a tsunami warning is issued to media and municipalities in regions where a tsunami is likely to&nbsp;hit.</li>
    <li>The Canadian Coast Guard's Marine Communications Traffic Services broadcasts tsunami alerts to mariners. </li>
    <li>When you get warning of a tsunami, if there is time, move to higher ground immediately.</li>
</ul>
<p>If you have any questions or need to know more about evacuation procedures, contact your local emergency management organization, or police or fire department.</p>
<h1>During a tsunami</h1>
<ul>
    <li>Do not go near the shore to watch a tsunami hit. If you can see it, you are too close to escape.</li>
    <li>Should a tsunami occur and you cannot get to higher ground, stay inside where you are protected from the water. It's best to be on the landward side of the house, away from windows. </li>
    <li>Often tsunamis occur in multiple waves that can occur minutes apart, but also as much as one hour apart. </li>
    <li>Monitor the tsunami's progress and listen for warnings or instructions from local officials. If you are safe when the first tsunami hits, stay put until authorities declare all is safe.</li>
    <li>After a tsunami hits, you may encounter flood waters. Flood waters can be dangerous to walk or drive through. Before driving anywhere, it is best to listen carefully to rescue officials who will be coordinating evacuation plans. </li>
    <li>Be aware of risks such as hypothermia from cold water or drowning from running water. </li>
</ul>


